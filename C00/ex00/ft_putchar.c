#include <unistd.h>

void	ft_putchar(char *c)
{
	int	i;

	i = 0;

	while(c[i] != '\0')
	{
		i++;
	}
	write(1, c, i);
}

int	main()
{
	ft_putchar("Lol");
	return (0);
}
